public class StringExercise {
    public static void main(String[] args) {
        String edu = "We don't need no education\nWe don't need no thought control";
        System.out.println(edu);

        String check =  "Check " +'"'+"this"+'"'+ " out!, " + '"'+ "s inside of" + '"' + "s!";
        System.out.println(check);

        String wind = "In windows, the main drive is usually C:\\";
        System.out.println(wind);

        String slash = "I can do backslashes \\," + "double backslashes \\\\,\n" +
        "and the amazing triple backslash \\\\\\!";

        System.out.println(slash);

//        I can do backslashes \, double backslashes \\,
//        and the amazing triple backslash \\\!


    }
}
